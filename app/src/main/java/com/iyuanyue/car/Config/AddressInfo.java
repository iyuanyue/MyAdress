package com.iyuanyue.car.Config;

public class AddressInfo {
    private String name;
    private String icon;
    private String poiname;
    private double lat;
    private double lon;

    public String getName() {
        return name;
    }

    public String getIcon() {
        return icon;
    }

    public String getPoiname() {
        return poiname;
    }

    public double getLat() {
        return lat;
    }

    public double getLon() {
        return lon;
    }
}
